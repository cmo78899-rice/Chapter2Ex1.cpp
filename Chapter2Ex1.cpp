#include <iostream>

int main () {
    // Declare variables to store the two numbers
    int num1 = 100;
    int num2 = 50;
    
    // Calculate the sum of the two numbers
    int sum = num1 + num2;
    
    // Display the result
    std::cout << "The sum of " << num1 << " and " << num2 << " is: " << sum << std::endl;
    
 // Return 0 to indicate successful program execution
 return 0;    
}